    private void actionCheck() {
        Bookmark m = MySwingUtilities.getSelectedObject(tblBookMark);
        if (m != null) {
            ((MangaDownloadGUI) getParent()).setCheckByBookmark(m);
        }
    }

    private void actionDelete() {
        Bookmark m = MySwingUtilities.getSelectedObject(tblBookMark);
        if (m != null) {
            BookmarkManager.removeBookmark(m);
            tblModel.fireTableDataChanged();
        }
    }

    public void reload() {
        tblModel.fireTableDataChanged();
    }
