    private void tblBookMarkMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBookMarkMouseReleased
        if (!chkEdit.isSelected()
                && MySwingUtilities.isDoubleLeftClick(evt)) {
            actionCheck();
        }
    }//GEN-LAST:event_tblBookMarkMouseReleased

    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        final String text = AccentRemover.removeAccent(txtSearch.getText()).toLowerCase();

        tblBookMark.setRowFilter(new RowFilter<TableModel, Integer>() {

            @Override
            public boolean include(RowFilter.Entry<? extends TableModel, ? extends Integer> entry) {
                for (int i = 0; i < entry.getValueCount(); i++) {
                    if (AccentRemover.removeAccent(entry.getValue(i).toString()).toLowerCase()
                            .contains(text)) {
                        return true;
                    }
                }
                return false;
            }
        });
    }//GEN-LAST:event_txtSearchKeyReleased

